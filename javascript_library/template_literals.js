/*
Template literals are string literals allowing embedded expressions.
You can use multi-line strings and string interpolation features with them.
A template literal begins and ends with backticks.
Code within a template literal is enclosed within ${}.
Template literals should contain interpolation or newlines.
*/

let a = 5;
let b = 10;

console.log(`The sum of a and b is ${a + b}`);

const big_string = `This big string he played one
He played nick nack on my drum
With a nick nack paddy whack
Give a dog a bone
This big string came rolling home`;

console.log(big_string);